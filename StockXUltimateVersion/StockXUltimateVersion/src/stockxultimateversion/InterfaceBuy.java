    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String brand = this.jTextField1.getText();
        String model = this.jTextField2.getText();
        this.jButton2.setVisible(false);
       
        ArrayList<Shoe> shoes = new ArrayList<>();
        if (!brand.isEmpty() && !model.isEmpty()) 
        {
            shoes = sh.searchShoesByBrand(brand);
            shoes.retainAll(sh.searchShoesByModel(model)); //mantiene solo ci√≤ che si trova sia in brand che in model (retainAll)
             
        }
        else if(!brand.isEmpty())
        {
        shoes = sh.searchShoesByBrand(brand);
        }
        else if(!model.isEmpty())
        {
        shoes = sh.searchShoesByModel(model);
        }
        else
        {
            this.jTextArea1.setText("Scarpa non trovata");
             this.jButton2.setVisible(false);
        }
        
        this.jTextArea1.append(shoes.toString());
        if(this.jTextArea1.getText().isEmpty())  this.jButton2.setVisible(false);
        else  this.jButton2.setVisible(true);
       
            
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

